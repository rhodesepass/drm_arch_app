/* ===== Text rendering ===== */

static void draw_char(uint8_t *dest, uint32_t line_len, int fb_bpp,
                      int scr_w, int scr_h, int cx, int cy, char ch, uint32_t color) {
    if (ch < 32 || ch > 126) ch = ' ';
    const uint8_t *glyph = font_8x16[ch - 32];

    for (int row = 0; row < FONT_H; row++) {
        int py = cy + row;
        if (py < 0 || py >= scr_h) continue;
        uint8_t bits = glyph[row];
        for (int col = 0; col < FONT_W; col++) {
            if (!(bits & (1 << (7 - col)))) continue;
            int px = cx + col;
            if (px < 0 || px >= scr_w) continue;

            if (fb_bpp == 4) {
                uint32_t *p = (uint32_t *)(dest + py * line_len + px * 4);
                *p = color;
            } else {
                /* RGB565 */
                uint16_t c565 = (uint16_t)(((color >> 19) & 0x1F) << 11 |
                                           ((color >> 10) & 0x3F) << 5  |
                                           ((color >>  3) & 0x1F));
                uint16_t *p = (uint16_t *)(dest + py * line_len + px * 2);
                *p = c565;
            }
        }
    }
}

static void draw_text_centered(uint8_t *dest, uint32_t line_len, int fb_bpp,
                               int scr_w, int scr_h, int cy, const char *text, uint32_t color) {
    int len = (int)strlen(text);
    if (len == 0) return;

    /*
     * Compute actual visual pixel boundaries of the full text string,
     * not just cell count * FONT_W. This eliminates the visual right-shift
     * caused by empty columns on the left/right edges of glyphs.
     */
    int first_px = len * FONT_W; /* will shrink */
    int last_px = 0;             /* will grow */
    for (int i = 0; i < len; i++) {
        char ch = text[i];
        if (ch < 32 || ch > 126) ch = ' ';
        const uint8_t *glyph = font_8x16[ch - 32];
        for (int row = 0; row < FONT_H; row++) {
            uint8_t bits = glyph[row];
            if (!bits) continue;
            for (int col = 0; col < FONT_W; col++) {
                if (bits & (1 << (7 - col))) {
                    int px = i * FONT_W + col;
                    if (px < first_px) first_px = px;
                    if (px > last_px) last_px = px;
                }
            }
        }
    }

    int visual_w = last_px - first_px + 1;
    int sx = (scr_w - visual_w) / 2 - first_px;

    for (int i = 0; i < len; i++) {
        draw_char(dest, line_len, fb_bpp, scr_w, scr_h, sx + i * FONT_W, cy, text[i], color);
    }
}

/* Read first line from a text file, return NULL if file doesn't exist */
static char *read_text_file(const char *path) {
    FILE *f = fopen(path, "r");
    if (!f) return NULL;
    static char buf[256];
    if (!fgets(buf, sizeof(buf), f)) { fclose(f); return NULL; }
    /* strip newline */
    buf[strcspn(buf, "\r\n")] = '\0';
    fclose(f);
    return buf[0] ? buf : NULL;
}

/* ===== Display helpers ===== */

static void unbind_vtconsole(void) {
    char path[64];
    for (int i = 0; i < 8; i++) {
        snprintf(path, sizeof(path), "/sys/class/vtconsole/vtcon%d/bind", i);
        int fd = open(path, O_WRONLY);
        if (fd >= 0) { write(fd, "0", 1); close(fd); }
    }
}

static inline uint32_t rgb565_to_xrgb8888(uint16_t c) {
    uint32_t r = (c >> 11) & 0x1F;
    uint32_t g = (c >>  5) & 0x3F;
    uint32_t b =  c        & 0x1F;
    r = (r << 3) | (r >> 2);
    g = (g << 2) | (g >> 4);
    b = (b << 3) | (b >> 2);
    return (0xFF << 24) | (r << 16) | (g << 8) | b;
}

static void blit_centered(uint8_t *dest, uint32_t line_len, int scr_w, int scr_h,
                          int fb_bpp, const uint8_t *px, int img_w, int img_h) {
    int off_x = (scr_w - img_w) / 2;
    int off_y = (scr_h - img_h) / 2;
    if (off_x < 0) off_x = 0;
    if (off_y < 0) off_y = 0;
    int cw = img_w < scr_w ? img_w : scr_w;
    int ch = img_h < scr_h ? img_h : scr_h;
    int sx = img_w > scr_w ? (img_w - scr_w) / 2 : 0;
    int sy = img_h > scr_h ? (img_h - scr_h) / 2 : 0;

    if (fb_bpp == SPLASH_BPP) {
        for (int y = 0; y < ch; y++) {
            memcpy(dest + (off_y + y) * line_len + off_x * fb_bpp,
                   px + (sy + y) * img_w * SPLASH_BPP + sx * SPLASH_BPP,
                   cw * SPLASH_BPP);
        }
    } else {
        for (int y = 0; y < ch; y++) {
            const uint16_t *src = (const uint16_t *)(px + (sy + y) * img_w * SPLASH_BPP + sx * SPLASH_BPP);
            uint32_t *dst = (uint32_t *)(dest + (off_y + y) * line_len + off_x * fb_bpp);
            for (int x = 0; x < cw; x++) {
                dst[x] = rgb565_to_xrgb8888(src[x]);
            }
        }
    }
}

/* ===== Main ===== */

int main(int argc, char *argv[]) {
    const char *path = DEFAULT_SPLASH;
    const char *text_path = DEFAULT_TEXT;
    int max_loops = -1;
    int hold_ms = 0;
    int bounded_mode = 0;

    for (int i = 1; i < argc; i++) {
        if (strcmp(argv[i], "--play-once") == 0) {
            bounded_mode = 1;
            if (max_loops < 0) max_loops = 1;
        } else if (strcmp(argv[i], "--max-loops") == 0) {
            if (i + 1 >= argc) return 2;
            max_loops = atoi(argv[++i]);
            bounded_mode = 1;
        } else if (strcmp(argv[i], "--hold-ms") == 0) {
            if (i + 1 >= argc) return 2;
            hold_ms = atoi(argv[++i]);
            bounded_mode = 1;
        } else if (strcmp(argv[i], "--text-file") == 0) {
            if (i + 1 >= argc) return 2;
            text_path = argv[++i];
        } else if (argv[i][0] == '-') {
            return 2;
        } else {
            path = argv[i];
        }
    }
    if (bounded_mode && max_loops < 0)
        max_loops = 1;

    signal(SIGTERM, sig_handler);
    signal(SIGINT, sig_handler);
    unbind_vtconsole();

    int fb_fd = -1;
    for (int retry = 0; retry < 50 && fb_fd < 0; retry++) {
        fb_fd = open(FB_DEV, O_RDWR);
        if (fb_fd < 0) usleep(100000);
    }
    if (fb_fd < 0) return 1;

    struct fb_var_screeninfo vi;
    struct fb_fix_screeninfo fi;
    ioctl(fb_fd, FBIOGET_VSCREENINFO, &vi);
    ioctl(fb_fd, FBIOGET_FSCREENINFO, &fi);

    int scr_w = vi.xres, scr_h = vi.yres, fb_bpp = vi.bits_per_pixel / 8;
    uint32_t visible_size = fi.line_length * scr_h;
    uint8_t *fb = mmap(NULL, fi.smem_len, PROT_READ | PROT_WRITE, MAP_SHARED, fb_fd, 0);
    if (fb == MAP_FAILED) { close(fb_fd); return 1; }

    memset(fb, 0, visible_size);

    /* Text overlay — read from file */
    char *overlay_text = read_text_file(text_path);
    int text_y = scr_h - TEXT_BOTTOM_MARGIN;
    uint32_t text_color = (0xFF << 24) | (TEXT_COLOR_R << 16) | (TEXT_COLOR_G << 8) | TEXT_COLOR_B;

    /* Read splash file */
    FILE *f = fopen(path, "rb");
    if (!f) {
        if (!bounded_mode) pause();
        munmap(fb, fi.smem_len);
        close(fb_fd);
        return bounded_mode ? 1 : 0;
    }

    fseek(f, 0, SEEK_END);
    long fsize = ftell(f);
    fseek(f, 0, SEEK_SET);

    if (fsize < 4) {
        fclose(f);
        if (!bounded_mode) pause();
        munmap(fb, fi.smem_len);
        close(fb_fd);
        return bounded_mode ? 1 : 0;
    }

    uint8_t magic[4];
    fread(magic, 1, 4, f);

    int animated = (magic[0] == 'E' && magic[1] == 'S' && magic[2] == 'P' && magic[3] == 'L');

    if (animated) {
        uint16_t hdr[4];
        if (fread(hdr, 1, 8, f) != 8) { fclose(f); return 1; }
        int img_w = hdr[0], img_h = hdr[1];
        int nframes = hdr[2];
        int delay_ms = hdr[3];
        if (nframes <= 0 || nframes > 1000) { fclose(f); return 1; }

        long frame_sz = (long)img_w * img_h * SPLASH_BPP;
        uint8_t *raw_frames = malloc(frame_sz * nframes);
        if (!raw_frames || fread(raw_frames, 1, frame_sz * nframes, f) != (size_t)(frame_sz * nframes)) {
            free(raw_frames); fclose(f); return 1;
        }
        fclose(f);

        /* Pre-render all frames with text overlay */
        uint8_t **prerendered = malloc(nframes * sizeof(uint8_t *));
        if (!prerendered) { free(raw_frames); munmap(fb, fi.smem_len); close(fb_fd); return 1; }

        for (int i = 0; i < nframes; i++) {
            prerendered[i] = malloc(visible_size);
            if (!prerendered[i]) {
                for (int j = 0; j < i; j++) free(prerendered[j]);
                free(prerendered); free(raw_frames);
                munmap(fb, fi.smem_len); close(fb_fd); return 1;
            }
            memset(prerendered[i], 0, visible_size);
            blit_centered(prerendered[i], fi.line_length, scr_w, scr_h, fb_bpp,
                          raw_frames + i * frame_sz, img_w, img_h);
            /* Draw text on every pre-rendered frame */
            if (overlay_text) {
                draw_text_centered(prerendered[i], fi.line_length, fb_bpp,
                                   scr_w, scr_h, text_y, overlay_text, text_color);
            }
        }
        free(raw_frames);

        /* Animation loop — only memcpy */
        int loops_done = 0;
        while (running && (max_loops < 0 || loops_done < max_loops)) {
            for (int i = 0; i < nframes && running; i++) {
                memcpy(fb, prerendered[i], visible_size);
                if (sleep_interruptible_ms(delay_ms) != 0)
                    break;
            }
            loops_done++;
        }

        if (hold_ms > 0)
            sleep_interruptible_ms(hold_ms);

        for (int i = 0; i < nframes; i++) free(prerendered[i]);
        free(prerendered);

    } else {
        /* Static */
        uint16_t img_w = magic[0] | (magic[1] << 8);
        uint16_t img_h = magic[2] | (magic[3] << 8);
        long expected = (long)img_w * img_h * SPLASH_BPP;

        if (img_w > 0 && img_h > 0 && img_w <= 4096 && img_h <= 4096 && expected == fsize - 4) {
            uint8_t *px = malloc(expected);
            if (px && fread(px, 1, expected, f) == (size_t)expected) {
                blit_centered(fb, fi.line_length, scr_w, scr_h, fb_bpp, px, img_w, img_h);
            }
            free(px);
        }
        fclose(f);

        /* Draw text on static splash */
        if (overlay_text) {
            draw_text_centered(fb, fi.line_length, fb_bpp,
                               scr_w, scr_h, text_y, overlay_text, text_color);
        }

        if (bounded_mode) {
            if (hold_ms > 0)
                sleep_interruptible_ms(hold_ms);
        } else {
            while (running) pause();
        }
    }

    munmap(fb, fi.smem_len);
    close(fb_fd);
    return 0;
}
