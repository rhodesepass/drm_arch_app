/*
 * fb-splash: Framebuffer boot splash for ArkEPass
 * Supports static images, frame-by-frame animation, and overlay text.
 * Runs until killed (SIGTERM) by LVGL UI service.
 *
 * .splash format (always RGB565 pixel data):
 *
 *   Static (no magic):
 *     [u16 width][u16 height][width*height*2 RGB565]
 *
 *   Animated (magic "ESPL"):
 *     [4B "ESPL"][u16 width][u16 height][u16 frames][u16 delay_ms]
 *     [frames * width*height*2 RGB565]
 *
 * Text overlay: reads /root/splash/splash.txt (first line, UTF-8 ASCII subset)
 *
 * Build: arm-...-gcc -O2 -o fb-splash fb-splash.c
 */

